            <p style="text-align: justify;">Berdasarkan Peraturan Daerah Nomor 3 Tahun 2016 tentang Pembentukan dan Susunan Organisasi 
            Perangkat Daerah dilingkungan Pemerintah Kota Singkawang, dan Peraturan Wali Kota Singkawang Nomor 14 Tahun 2021 tentang Kedudukan,
             Susunan Organisasi, Tugas dan Fungsi serta Tata Kerja Dinas Komunikasi dan Informatika Kota Singkawang, adalah sebagai berikut :</p>
             
             <h2 class="mb-3" style="text-align: center;">KEPALA DINAS</h2>
            <p><ol>a. Tugas Pokok</ol>
            <ol style="text-align: justify;">Dinas Komunikasi dan Informatika Kota Singkawang mempunyai tugas
            membantu Wali Kota melaksanakan urusan pemerintahan yang menjadi
            kewenangan Daerah dan tugas pembantuan di Bidang Komunikasi dan
            Informatika.</ol>
            <ol style="text-align: justify;">Dinas Komunikasi dan Informatika dipimpin oleh seorang Kepala Dinas
            sebagai unsur pimpinan yang mempunyai tugas Memimpin, Mengoordinasikan,
            Menyelenggarakan, mengevaluasi dan pelaporan kegiatan dibidang Komunikasi
            dan Informatika, statistik serta persandian sesuai peraturan Per UndangUndangan.</ol>
            </p>

            <p><ol>b. Fungsi</ol>
            <ol style="text-align: justify;">Untuk melaksanakan tugas pokok tersebut Dinas Komunikasi dan Informatika Kota Singkawang mempunyai fungsi sebagai berikut :</ol>
            <ol>
                <li>Perumusan Kebijakan di Bidang Komunikasi dan Informatika, statistik serta persandian;</li>
                <li>Pelaksanaan Kebijakan di Bidang Komunikasi dan Informatika, statistik serta persandian;</li>
                <li>Pelaksanaan Evaluasi dan Pelaporan di Bidang Komunikasi dan Informatika, statistik serta persandian;</li>
                <li>Pelaksanaan Administrasi Dinas Komunikasi dan Informatika; dan</li>
                <li>Pelaksanaan fungsi lain yang diberikan oleh Wali Kota terkait dengan tugas dan fungsinya.</li>
            </ol>
            </p>

            <h2 class="mb-3" style="text-align: center;">SEKRETARIS DINAS</h2>
            <p><ol>a. Tugas Pokok</ol>
            <ol style="text-align: justify;">Melaksanakan sebagian tugas Dinas dalam penyusunan program kerja, \
            menyiapkan bahan rumusan kebijakan, koordinasi, fasilitasi, monitoring, evaluasi dan pelaporan pelaksanaan 
            kegiatan kesekretariatan meliputi umum, kepegawaian dan aset, perencanaan evaluasi kerja, serta keuangan.</ol>
            <ol style="text-align: justify;">Sekretariat dipimpin oleh sekretaris yang berkedudukan dibawah dan 
            bertanggung jawab kepada Kepala Dinas.</ol>
            </p>

            <p><ol>b. Fungsi</ol>
            <ol>
                <li>Penyusunan program kerja bidang, pelaksanaan, monitoring, evaluasi dan pelaporan kesekretariatan.</li>
                <li>Penyiapan bahan perumusan kebijakan teknis kesekretariatan.</li>
                <li>Penyiapan bahan pelaksanaan kebijakan kesekretariatan.</li>
                <li>Penyiapan bahan evaluasi dan pelaporan kesekretariatan.</li>
                <li>Pelaksanaan fungsi lain yang diberikan oleh Kepala Dinas sesuai dengan tugas dan fungsinya.</li>
            </ol>
            <ol style="text-align: justify;">Dalam menjalankan tugas dan fungsi di atas, Sekretariat dibantu oleh 2
            (dua) Sub Bagian yang dipimpin oleh seorang Kepala Sub Bagian yang
            berkedudukan dan bertanggung jawab kepada Sekretaris Yaitu : Sub Bagian
            Umum, Kepegawaian dan Aset, serta Sub Bagian Perencanaan, Evaluasi Kinerja,
            dan Keuangan yang masing-masing memiliki tugas sebagai berikut :
            </ol>

            <h3 class="mb-3" style="text-align: justify;">1. SUB BAGIAN UMUM, KEPEGAWAIAN DAN ASET</h3>
            <ol>Mempunyai tugas :</ol>
            <ol>Melaksanakan sebagian tugas Sekretariat yang meliputi :</ol>
            <ol>
                <li>menyusun rencana kerja, mengumpul dan mengolah bahan kerja, koordinasi, fasilitasi</li>
                <li>menyusun pelaksanaan urusan umum, kepegawaian dan aset yang meliputi tata
                persuratan, kearsipan, organisasi, ketatalaksanaan, hukum dan perundangundangan, 
                administrasi perjalanan dinas, kehumasan dan protokol, rumah tangga, Laporan Harta Kekayaan Penyelenggara Negara (LHKPN),
                </li>
                <li>Laporan Harta Kekayaan Aparatur Sipil Negara (LHKASN), penyiapan bahan reformasi birokrasi, pelaksanaan Sistem Pengendalian Intern
                Pemerintah (SPIP) dan pengelolaan adminsitrasi kepegawaian.
                </li>  
            </ol>
            
            <h3 class="mb-3" style="text-align: justify;">2. SUB BAGIAN PERENCANAAN, EVALUASI KINERJA, DAN KEUANGAN</h3>
            <ol>Mempunyai tugas :</ol>
            <ol>
                <li>Menyusun rencana kerja, mengumpul dan mengolah bahan kerja,</li>
                <li>koordinasi, fasilitasi pelaksanaan urusan bidang perencanaan, evaluasi kinerja</li>
                <li>dan keuangan meliputi penyusunan program dan anggaran (RKT, RKA)</li>
                <li>rencana strategis, perbendaharaan dan gaji, verifikasi dan akuntansi</li>
                <li>laporan keuangan, penyusunan laporan kinerja tahunan (LKT)</li>
                <li>Menyiapkan Bahan penyusunan Laporan Penyelenggaraan Pemerintahan Daerah (LPPD) dan</li>
                <li>Laporan Keterangan Pertanggung Jawaban (LKPJ), tindak lanjut hasil pemeriksaan, serta pengelolaan dan inventarisasi barang milik daerah.</li>
            </ol>

            <h2 class="mb-3" style="text-align: center;">BIDANG INFORMASI DAN KOMUNIKASI PUBLIK</h2>
            <ol>a. Tugas Pokok</ol>
            <ol style="text-align: justify;">Menyusun program kerja, menyiapkan bahan perumusan kebijakan teknis, koordinasi, fasilitasi, monitoring, 
            evaluasi dan pelaporan pelaksanaan kegiatan bidang informasi dan komunikasi publik meliputi pengelolaan informasi publik dan pengelolaan komunikasi publik.</ol>
            <ol style="text-align: justify;">Bidang Informasi dan Komunikasi Publik dipimpin oleh seorang Kepala
            Bidang yang berkedudukan di bawah dan bertanggung-jawab kepada Kepala Dinas.</ol>
            <ol>b. Fungsi</ol>
            <ol>
                <li>Penyusunan program kerja bidang, pelaksanaan, monitoring, evaluasi dan
                pelaporan bidang informasi dan komunikasi publik.</li>
                <li>Penyiapan bahan perumusan kebijakan teknis, bidang pengelolaan
                informasi dan pengelolaan komunikasi publik.</li>
                <li>Penyiapan bahan pelaksanaan kebijakan teknis, bidang pengelolaan
                informasi publik dan pengelolaan komunikasi publik.</li>
                <li>Penyiapan bahan evaluasi dan pelaporan bidang pengelolaan informasi
                publik dan pengelolaan komunikasi publik.</li>
                <li>Pelaksanaan fungsi lain yang diberikan oleh Kepala Dinas sesuai dengan
                tugas dan fungsinya.</li>
            </ol>
            <ol style="text-align: justify;">Dalam menjalankan Tugas dan fungsi diatas, Bidang Informasi dan
            Komunikasi Publik dibantu oleh 2 (dua) Seksi yang dipimpin oleh seorang Kepala Seksi yang berkedudukan 
            dibawah dan bertanggung Jawab kepada Kepala Bidang Yaitu : Seksi Pengelolaan Informasi Publik, dan Seksi
            Pengelolaan Komunikasi Publik, yang masing-masing memiliki tugas sebagai berikut ;</ol>

            <h2 class="mb-3" style="text-align: justify;">1. SEKSI PENGELOLAAN INFORMASI PUBLIK</h2>
            <ol>Mempunyai tugas :</ol>
            <ol>
                <li>Menyusun rencana kerja, mengumpul dan mengolah bahan kerja,</li>
                <li>koordinasi, fasilitasi pelaksanaan bidang Pengelolaan informasi publik.</li>
            </ol>
            <h2 class="mb-3" style="text-align: justify;">2. SEKSI PENGELOLAAN KOMUNIKASI PUBLIK</h2>
            <ol>Mempunyai tugas :</ol>
            <ol>
                <li>Menyusun rencana kerja, mengumpul dan mengolah bahan kerja,</li>
                <li>koordinasi, fasilitasi pelaksanaan bidang pengelolaan komunikasi publik.</li>
            </ol>

            <h2 class="mb-3" style="text-align: center;">BIDANG APLIKASI DAN INFORMATIKA</h2>
            <ol>a. Tugas Pokok</ol>
            <ol style="text-align: justify;">Menyusun program kerja, menyiapkan bahan perumusan kebijakan
            teknis, koordinasi, fasilitasi, monitoring, evaluasi dan pelaporan pelaksanaan
            kegiatan bidang Aplikasi dan Informatika meliputi infrastruktur dan layanan egovernment, pengembangan aplikasi 
            Informatika. Bidang Aplikasi dan Informatika dipimpin oleh seorang Kepala Bidang yang berkedudukan dibawah dan 
            bertanggung jawab kepada Kepala Dinas</ol>
            <ol>b. Fungsi</ol>
            <ol>
                <li>Penyusunan program kerja bidang, pelaksanaan, monitoring, evaluasi dan pelaporan kegiatan bidang aplikasi dan informatika.</li>
                <li>Penyiapan bahan perumusan kebijakan teknis, bidang infrastruktur dan layanan e-government serta pengembangan aplikasi informatika.</li>
                <li>Penyiapan bahan pelaksanaan kebijakan bidang infrastruktur dan layanan e-government serta pengembangan aplikasi informatika.</li>
                <li>Pelaksanaan tugas lain yang diberikan oleh Kepala Dinas sesuai dengan tugas dan fungsinya.</li>
            </ol>
            <ol style="text-align: justify;">Dalam menjalankan tugas dan fungsi diatas, Bidang E-Government dan
            Telematika dibantu oleh 2 (dua) Seksi yang dipimpin oleh seorang Kepala Seksi,
            yang berkedudukan dibawah dan bertanggung jawab kepada Kepala Bidang, Yaitu : Seksi Infrastruktur dan layanan e-government, dan Seksi pengembangan
            aplikasi informatika, yang masing-masing memiliki tugas sebagai berikut:</ol>
        
            <h2 class="mb-3" style="text-align: justify;">1. SEKSI INFRASTRUKTUR DAN LAYANAN E-GOVERNMENT</h2>
            <ol>Mempunyai tugas :</ol>
            <ol>
                <li>Menyusun rencana kerja, mengumpul dan mengolah bahan kerja,</li>
                <li>koordinasi, fasilitasi pelaksanaan bidang Infrastruktur dan Layanan E-government.</li>
            </ol>
            <h2 class="mb-3" style="text-align: justify;">2. SEKSI PENGEMBANGAN APLIKASI INFORMATIKA</h2>
            <ol>Mempunyai tugas :</ol>
            <ol>
                <li>Menyusun rencana kerja, mengumpul dan mengolah bahan kerja,</li>
                <li>koordinasi, fasilitasi pelaksanaan bidang pengembangan aplikasi informatika.</li>
            </ol>
            
            <h2 class="mb-3" style="text-align: center;">BIDANG STATISTIK DAN PERSANDIAN</h2>
            <ol>a. Tugas Pokok</ol>
            <ol style="text-align: justify;">Menyusun program kerja, menyiapkan bahan perumusan kebijaka,
            koordinasi, fasilitasi, monitoring, evaluasi dan pelaporan pelaksanaan kegiatan
            bidang statistik dan persandian meliputi statistik sektoral dan persandian.
            Bidang Statistik dan Persandian dipimpin oleh seorang Kepala Bidang
            yang berkedudukan dibawah dan bertanggung jawab kepada Kepala Dinas</ol>
            <ol>b. Fungsi</ol>
            <ol>
                <li>Penyusunan program kerja bidang, pelaksanaan, monitoring, evaluasi dan pelaporan kegiatan bidang statistik dan persandian.</li>
                <li>Penyiapan bahan perumusan kebijakan teknis, bidang statistik dan persandian.</li>
                <li>Penyiapan bahan pelaksanaan kebijakan bidang statistik sektoral dan persandian.</li>
                <li>Pelaksanaan tugas lain yang diberikan oleh Kepala Dinas sesuai dengan tugas dan fungsinya.</li>
            </ol>
            <ol style="text-align: justify;">Dalam menjalankan tugas dan fungsi diatas, Bidang E-Government dan
            Telematika dibantu oleh 2 (dua) Seksi yang dipimpin oleh seorang Kepala Seksi,
            yang berkedudukan dibawah dan bertanggung jawab kepada Kepala Bidang, Yaitu : Seksi Statistik Sektoral, 
            dan Seksi Persandian, yang masing-masing memiliki tugas sebagai berikut:</ol>
        
